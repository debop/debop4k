package debop4k.core;

public class NotSupportedException extends RuntimeException {

  public NotSupportedException() { super(); }

  public NotSupportedException(String msg) { super(msg); }

  public NotSupportedException(String msg, Throwable cause) { super(msg, cause); }

  public NotSupportedException(Throwable cause) { super(cause); }

  private static final long serialVersionUID = 8828601657527505187L;
}
