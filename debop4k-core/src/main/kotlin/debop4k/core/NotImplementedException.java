package debop4k.core;

public class NotImplementedException extends RuntimeException {

  public NotImplementedException() { super(); }


  public NotImplementedException(String msg) { super(msg); }

  public NotImplementedException(String msg, Throwable cause) { super(msg, cause); }

  public NotImplementedException(Throwable cause) { super(cause); }

  private static final long serialVersionUID = 5528679465757797650L;
}
